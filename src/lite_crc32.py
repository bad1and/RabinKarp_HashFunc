def rolling_crc32(s: str, crc: int = 0) -> int:
    name = "Lite_CRC32"
    """
    УПРОЩЁННЫЙ CRC32 ДЛЯ СКОЛЬЗЯЩЕГО ХЕШИРОВАНИЯ
    Основан на циклическом избыточном коде.

    Плюсы:
    - Отличное распределение
    - Обнаруживает ошибки в данных
    - Используется в сетях и файловых системах

    Минусы:
    - Сложнее для понимания
    - Медленнее полиномиального

    Особенность: легко пересчитывать при добавлении/удалении байта
    """
    poly = 0xEDB88320  # стандартный полином CRC32

    for char in s:
        byte = ord(char)
        crc ^= byte
        for _ in range(8):
            if crc & 1:
                crc = (crc >> 1) ^ poly
            else:
                crc >>= 1

    return crc


# Альтернатива: использовать готовую, но без скользящей оптимизации
def crc32_builtin(s: str) -> int:
    """Встроенная CRC32 (для сравнения)"""
    import zlib
    return zlib.crc32(s.encode()) & 0xFFFFFFFF