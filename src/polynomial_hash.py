def polynomial_hash(s: str, p: int = 31, m: int = 10 ** 9 + 7) -> int:
    name = "Polynomial_Hash"
    """
    КЛАССИЧЕСКИЙ ПОЛИНОМИАЛЬНЫЙ ХЕШ
    Используется в оригинальном алгоритме Рабина-Карпа.

    Плюсы:
    - Отличное распределение
    - Очень мало коллизий
    - Идеален для скользящего хеширования

    Минусы:
    - Нужно выбирать хорошие p и m
    - Медленнее простых сумм

    Формула: Σ char_i * p^(n-i-1) mod m
    Скользящий хеш: h_new = ((h_old - char_out * p^(n-1)) * p + char_in) mod m
    """
    h = 0
    for char in s:
        h = (h * p + ord(char)) % m
    return h